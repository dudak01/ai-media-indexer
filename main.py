import sys
import os

# Добавляем корень проекта в sys.path, чтобы импорты работали корректно
# при запуске из любой директории
BASE_DIR = os.path.dirname(os.path.abspath(__file__))
if BASE_DIR not in sys.path:
    sys.path.insert(0, BASE_DIR)


def main():
    """
    Запуск GUI-приложения с обработкой ошибок инициализации.
    Все ошибки уровня импорта или старта будут показаны пользователю
    с понятным объяснением, что нужно сделать.
    """
    try:
        from ui.app import run_gui
        run_gui()
    except ImportError as e:
        print(f"[ОШИБКА ИМПОРТА] Не удалось загрузить модули приложения: {e}")
        print("Убедитесь, что все зависимости установлены:")
        print("    pip install -r requirements.txt")
        sys.exit(1)
    except Exception as e:
        print(f"[КРИТИЧЕСКАЯ ОШИБКА ЗАПУСКА] {e}")
        sys.exit(1)


if __name__ == "__main__":
    main()